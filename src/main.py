def add_numbers(a, b):
    """_summary_

    Args:
        a (float): Real number
        b (float): Real number

    Returns:
        float: Real number (addition of a and b)
    """
    return a + b

def multiply_numbers(a, b):
    """_summary_

    Args:
        a (float): Real number
        b (float): Real number

    Returns:
        float: Real number (multiplication of a and b)
    """
    return a * b